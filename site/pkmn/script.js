//Validate input
function validate() {
    
  var success = true;

  $('.input').each(function(i, item) {
      if ($(item).val() === "") 
      {
         console.log("Missing textarea input");
         success = false;
         $(item).attr("style","border:1px solid red;");
      }
      else
      {
         $(item).removeAttr('style');
      }
   });
  return success;
}
